package `1_tipos_de_variables`

fun main() {
    // variable inmutable de tipo entero
    val edad: Int
    edad = 48
    //variable inmutable de tipo decimal
    val sueldo: Float
    sueldo = 1200.55f // siempre lleva f al final del valor
    // variable inmutable de tipo decimal
    val total: Double
    total = 70000.24
    //variable inmutable de tipo alfanumerico
    val titulo: String
    titulo = "sistema de ventas"
    //variable mutable
    var mes: Int
    mes = 1
    // algunas lineas de codigo
    mes = 2
    //variable de tipo Char
    var continua: Char = 's'
// variable de tipo booleano, true or flase
    var fin: Boolean = false
    print("flores")
    print("camacho")
}
